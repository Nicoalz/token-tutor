const getChainContract = (chainId: number) => {
  const chainContract = contractChains.find(
    (chain) => chain.chainId === chainId
  );
  if (!chainContract) {
    throw new Error(`No chain contract found for chainId: ${chainId}`);
  }
  return chainContract;
};

export const getMainContract = async (signer: Signer) => {
  const chainId = await signer.getChainId();
  const chainContract = getChainContract(chainId);
  return new Contract(chainContract.main, MainContractABI, signer);
};

export const getTokenContract = async (signer: Signer) => {
  const chainId = await signer.getChainId();
  const chainContract = getChainContract(chainId);
  return new Contract(chainContract.token, ERC20ABI, signer);
};

export const hasApproved = async (signer: Signer | undefined) => {
  if (!signer) {
    throw new Error("No signer found");
  }
  const chainId = await signer.getChainId();
  const contract = await getTokenContract(signer);
  const chainContract = getChainContract(chainId);
  const approved = await contract.allowance(
    await signer.getAddress(),
    chainContract.main
  );
  return approved > 0;
};

export const approve = async (signer: Signer | undefined) => {
  if (!signer) {
    throw new Error("No signer found");
  }
  const chainId = await signer.getChainId();
  const contract = await getTokenContract(signer);
  const chainContract = getChainContract(chainId);
  await contract.approve(chainContract.main, "10000000000000000000000");
};

